
public class Calculator
{
	public int addInt(int num1,int num2)
	{
		return num1+num2;
	}
	
	public int subtractInt(int num1,int num2)
	{
		return num1-num2;
	}
	
	public float divisionInt(int num1,int num2)
	{
		return num1/num2;
	}
	
	public int multiplicationInt(int num1,int num2)
	{
		return num1*num2;
	}
	
	public float addFloat(float num1,float num2)
	{
		return num1+num2;
	}
	
	public float subtractFloat(float num1,float num2)
	{
		return num1-num2;
	}
	
}
