package org;

public class Courses
{
	String courseName;
	
	public Courses(){}

	public Courses(String courseName)
	{
		super();
		this.courseName = courseName;
	}

	public String getCourseName()
	{
		return courseName;
	}

	public void setCourseName(String courseName)
	{
		this.courseName = courseName;
	}
	
	
}
